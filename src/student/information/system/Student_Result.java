    private void jbtnRanking1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRanking1ActionPerformed
        double[] R = new double[14];
        
        R[0] = Double.parseDouble(jtxtEng.getText());
        R[1] = Double.parseDouble(jtxtMath.getText());
        R[2] = Double.parseDouble(jtxtSci.getText());
        R[3] = Double.parseDouble(jtxtEnt.getText());
        R[4] = Double.parseDouble(jtxtAnimation.getText());
        R[5] = Double.parseDouble(jtxtJava.getText());
        R[6] = Double.parseDouble(jtxtPe.getText());
        
        R[7] = (R[0] +R[1] + R[2] + R[3] + R[4] + R[5] + R[6]) /7;
        R[9] = R[0] + R[1] + R[2] + R[3] + R[4] + R[5] + R[6];
              
             String Average = String.format("%.0f", R[7]);
             jtxtAverage.setText(Average);
             
             String TotalScore = String.format("%.0f", R[9]);
             jtxtTotal.setText(TotalScore);
             
             if (R[7] >=90)
             {
                 jtxtRank.setText("1st");
             }
             else if (R[7] >= 88)
             {
                 jtxtRank.setText("2nd");
             }
             else if (R[7] >= 85)
             {
                 jtxtRank.setText("3rd");
             }
             else if (R[7] >= 80)
             {
                 jtxtRank.setText("4th");
             }
             else if (R[7] >= 60)
             {
                 jtxtRank.setText("5th");
             }
             
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
             model.addRow(new Object[]{
                 
                 jtxtStudentID.getText(),
                 jcmbCourseCode.getSelectedItem(),
                 jtxtEng.getText(),
                 jtxtMath.getText(),
                 jtxtSci.getText(),
                 jtxtEnt.getText(),
                 jtxtAnimation.getText(),
                 jtxtJava.getText(),
                 jtxtPe.getText(),
                 jtxtTotal.getText(),
                 jtxtAverage.getText(),
                 jtxtRank.getText(),
                     
             
             });
    }//GEN-LAST:event_jbtnRanking1ActionPerformed

